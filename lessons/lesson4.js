//Relational or comparison operators
// >, <, >=, <=, ==, ===, !=, !==

// > Greater than
// < Less than
// >= Greater than or equal to
// <= Less than or equal to

var result = 10 > 5; //true
console.log(result);

//Ewuality operators
// == Equal to (loose equality)
// === Strictly equal to (strict equality)
// != Not equal to (loose inequality)
// !== Not strictly equal to (strict inequality)

var x = 1;
console.log(x == '1'); //true (loose equality) means that the value is the same, but not necessarily the type
console.log(x === '1'); //false (strict equality) mneans that the value and the type are the same